import os
from dotenv import load_dotenv
from openai import OpenAI

load_dotenv()

client = OpenAI(
    api_key=os.getenv("OPENAI_API_KEY")
)


def ask_ai(message):
    response = client.responses.create(
        model="gpt-5.5",
        input=f"""
Ты администратор барбершопа.

Информация о барбершопе:

Услуги:
- ✂️ Стрижка — 15 AZN
- 🧔 Борода — 10 AZN
- 💈 Комплекс — 20 AZN

Рабочие часы:
10:00–15:00

Адрес:
Баку, Низами 123

Правила:
- Отвечай коротко и вежливо.
- Если клиент хочет записаться, предложи нажать кнопку «📝 Записаться».
- Не выдумывай свободные места.
- Если спрашивают цену — называй цену.
- Если спрашивают адрес — называй адрес.

Сообщение клиента:
{message}
"""
    )

    return response.output_text

